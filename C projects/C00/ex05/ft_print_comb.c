#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

//Scrive la virgola e lo spazio se non Ã¨ l'ultima combinazione
void	ft_checklastcase(char i, char j, char k)
{
	if (!(i == '7' && j == '8' && k == '9'))
	{
		ft_putchar(',');
		ft_putchar(' ');
	}
}

void	ft_print_comb(void)
{
	char	i;
	char	j;
	char	k;

	i = '0';
	while (i <= '7')
	{
		j = i + 1;
		while (j <= '8')
		{
			k = j + 1;
			while (k <= '9')
			{
				ft_putchar(i);
				ft_putchar(j);
				ft_putchar(k);
				ft_checklastcase(i, j, k);
				k++;
			}
			j++;
		}
		i++;
	}
}
